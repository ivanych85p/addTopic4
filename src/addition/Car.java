package addition;

public class Car {
    String brand;
    String model;
    int yearProduction;
    int engineCapacity;
    String fuel;
    String transmission;
    int mileageOfCar;



    public Car(String brand, String model, int yearProduction, int engineCapacity, String fuel,
               String transmission, int mileageOfCar) {
        this.brand = brand;
        this.model = model;
        this.yearProduction = yearProduction;
        this.engineCapacity = engineCapacity;
        this.fuel = fuel;
        this.transmission = transmission;
        this.mileageOfCar = mileageOfCar;
    }







}
